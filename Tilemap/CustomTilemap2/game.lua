game.tileSheet = nil
game.tileQuad = {}
game.tileTypes = {}

game.load = function()
    game.tileSheet = love.graphics.newImage("_Images_/tilesheet.png")
    local nbColumns = game.tileSheet:getWidth()/TILE_WIDTH
    local nbLines = game.tileSheet:getHeight()/TILE_HEIGHT
    local id = 1
    game.tileQuad[0] = nil
    for l=1,nbLines do
        for c=1, nbColumns do
            game.tileQuad[id] = love.graphics.newQuad((c-1) * TILE_WIDTH, (l-1) * TILE_HEIGHT, TILE_WIDTH, TILE_HEIGHT, game.tileSheet:getWidth(), game.tileSheet:getHeight() )
            id = id + 1
        end
    end
    game.tileTypes[10] = "Grass"
    game.tileTypes[11] = "Grass"
    game.tileTypes[13] = "Sand"
    game.tileTypes[14] = "Sand"
    game.tileTypes[15] = "Sand"


end

game.draw = function()
    for l=1,MAP_HEIGHT do
        for c=1,MAP_WIDTH do
            local id = game.map[l][c]
            local quad = game.tileQuad[id]
            if quad then
                love.graphics.draw(game.tileSheet, quad, (c-1)*TILE_WIDTH, (l-1)*TILE_HEIGHT)
            end
        end
    end

    local x, y = love.mouse.getPosition()
    local column = math.floor(x / TILE_WIDTH) + 1
    local line = math.floor(y/TILE_HEIGHT) + 1
    if column > 0 and column <= MAP_WIDTH and line > 0 and line <= MAP_HEIGHT then
        local id = game.map[line][column]
        love.graphics.print("type: "..tostring(game.tileTypes[id]), x+15, y)
    end
end

return game